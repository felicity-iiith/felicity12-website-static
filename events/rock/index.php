 <div id="format" style="display:none;font-size:20px;">
 <b>Band registration is completed in 2 steps:</b><br><br>
<p>1. Fill up the registration form.<br><br>
<p>2. Upload at least 2 demos with minimum one original composition.<br><br>

<b>NOTE:</b> Bad quality of demos can effect short listing of bands eligible to compete in regional round. A studio version of the song is recommended, however not compulsory. Uploading band profile is advised.<br><br>
Registrations close on 15th Feb 2012 at 2359 hrs. Demos submitted after this deadline won't be considered for judging.<br><br>
The registration forms are not valid without demos and similarly the demos are not valid without the registration forms.<br><br>

<p><b>Event schedule:</b><br><br>
<p>1) Citywise eliminations<br><br>
 Hyderabad : 17th Feb (IIIT Hyderabad)<br></p><br><br>
 
<p>2) Finals, Hyderabad: 18th Feb, Main Stage, IIIT Hyderabad</p><br><br>

<p><b>Competition format:</b><br><br>
1) Selection through demos: Out of all the registered bands, 10 bands from each city would be selected for the regional eliminations on the basis of their demos <br><br>
2) Regional eliminations: Out of 10 bands competing in regional eliminations, 3 bands from Hyderabad and 1 band from Bangalore would be selected for the finals.<br><br>
3) Each team will be provided 20 mins(in prelims) from AMP ON to AMP OFF, ie: including your setup time.<br><br>
4) Finals: 4 Finalists will compete against each other while doing an opening act for a famous International/National Headliner.<br><br>
5) All the finalists will be given 35 mins(including everything) to perform.
</p>
<br><br>

<p><b>Incentives:</b><br><br>
1) Cash Prize of worth 25000 INR to the winners.<br><br>
2) Opportunity to open for a famous International/National headliner.<br><br>
3) Opportunity to be signed on the Metamorphik Productions label.<br><br></p>

